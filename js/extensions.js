Array.prototype.fromObjectList = function(objectList){
    const result = [];
	const keys = Object.keys(objectList);
	for (let prop in keys) {
      	if (hasOwnProperty.call(keys, prop)) {
        	let item = objectList[keys[prop]];
        	result.push(item); 
      	}
	}
	return result;
};

